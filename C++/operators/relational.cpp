#include <iostream>
using namespace std;

int main() {
    cout << "----Relational Operators----" << endl;

    cout << (10 > 9) << endl;
    cout << (10 < 9) << endl;
    cout << (10 >= 9) << endl;
    cout << (10 <= 9) << endl;
    cout << (10 != 9) << endl;
    cout << (10 == 9) << endl;
    return 0;
}